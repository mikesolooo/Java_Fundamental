package java_20190607;

import java.util.HashSet;
import java.util.Iterator;
import java.util.LinkedHashSet;
import java.util.TreeSet;

public class SetDemo {
	public static void main(String[] args) {
		//자동 import : ctrl + shift + o
		//HashSet은 데이터의 중복을 허용하지 않고, 순서 없음.
		//HashSet<String> set1 = new HashSet<String>();
		
		//TreeSet은 데이터의 중복을 허용하지 않고, natural ordering(오름차순 정렬)
		//TreeSet<String> set1 = new TreeSet<String>();
		
		//LinkedHashSet은 데이터의 중복을 허용하지 않고, 입력한 순서대로 출력
		LinkedHashSet<String> set1 = new LinkedHashSet<String>();
		
		//데이터 추가
		for(int i=0;i<20;i++) {
			//valueOf(int i) 메소드는 int i 를 문자열로 변환 1 => "1"
			set1.add(String.valueOf(i));
		}
		//HashSet 크기
		System.out.println(set1.size());
		
		//데이터 출력, iterator 뜻: 반복자, 반복자를 끄집어낸다 
		//hasNext로 있냐 물어보고 있으면 next로 끄집어내는 코딩 
		//iterator로 끄집어 내는건 모두 이렇게 끄집어낸다. 
		Iterator<String> iterator = set1.iterator();
		while(iterator.hasNext()) {
			String temp = (String)iterator.next();
			System.out.println(temp);
		}
		
		Customer c1 = new Customer("홍길동",10);
		Customer c2 = new Customer("홍길동",10);
		Customer c3 = new Customer("홍길동",10);
		Customer c4 = new Customer("홍길동",10);
		//모두 지우기
		set1.clear();
		
		HashSet<Customer> set2 = new HashSet<Customer>();
		set2.add(c1);
		set2.add(c2);
		set2.add(c3);
		set2.add(c4);
		
		System.out.println(set2.size());
		
		//모두 지우기
		//set1.clear();
		
		TreeSet<Integer> set3 = new TreeSet<Integer>();
		
		while(true) {
			int random = (int)(Math.random()*45)+1;
			set3.add(random);
			if(set3.size()==6) break;
		}
		
		Iterator<Integer> i = set3.iterator();
		while(i.hasNext()) {
			Integer temp = i.next();
			System.out.print(temp+"\t");
		}
		
	}
}

class Customer {
	private String name;
	private int age;
	public Customer(String name,int age) {
		super();
		this.name = name ; 
		this.age = age ; 
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + age;
		result = prime * result + ((name == null) ? 0 : name.hashCode());
		return result;
	}
	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		Customer other = (Customer) obj;
		if (age != other.age)
			return false;
		if (name == null) {
			if (other.name != null)
				return false;
		} else if (!name.equals(other.name))
			return false;
		return true;
	}
	
}